class areaOfCircle{
	public static void main(String[] args) {
		double rad;
		final double PI = 3.14159;
		rad = 10.0;
		// Area of circle formulae: A=PI*r^2
		double area = PI * rad * rad;
		System.out.println("Area of circle: "+area);
		
	}
}

